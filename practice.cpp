        if(!head){
            return head;
        }
        ListNode*eve = head->next;
        ListNode* temp = eve;
        ListNode*odd = head;
        while(odd->next && eve->next){
            odd->next = odd->next->next;
            odd = odd->next;
            eve->next = eve->next->next;
            eve = eve->next;
        }
        odd->next = temp;
        return head;
