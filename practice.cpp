    int numOfWays(int n)
    {
        long three = 6, two = 6;
        long mod = 10e9 + 7;
        for (int i = 2; i <= n; i++)
        {
            long temp = three;
            three = (2 * two + 2 * three) % mod;
            two = (2 * temp + 3 * two) % mod;
        }
        return (two + three) % mod;
    }
