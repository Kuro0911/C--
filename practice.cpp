int BinSearch(int st, int ed, int val, vector<int> arr)
{
    if (st > ed)
    {
        return -1;
    }
    int md = (st + ed) / 2;
    if (arr[md] == val)
    {
        return md;
    }
    if (arr[md] >= val)
    {
        return BinSearch(st, md - 1, val, arr);
    }
    return BinSearch(md + 1, ed, val, arr);
}
